func TestValidateIntent_MultipleIssuesReturnsFirst(t *testing.T) {
	// When multiple issues exist, we return the first one (in validation order)
	// Validation order: Action -> Target.Kind -> Target.Name -> Target.Namespace -> Reason
	intent := &ParsedIntent{
		Action: ActionUpdate,
		Target: ResourceTarget{
			// Missing Kind, Name, and Namespace
		},
		RiskLevel: RiskHigh,
	}

	result := ValidateIntent(intent)
	if result == nil {
		t.Fatal("expected ClarifyQuestion for invalid intent, got nil")
	}
	// First validation after Action is Target.Kind check (since ActionUpdate is valid)
	if result.Field != "target.kind" {
		t.Errorf("expected first error on 'target.kind' field, got %s", result.Field)
	}
}

