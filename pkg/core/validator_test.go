func TestDefaultRiskLevel(t *testing.T) {
	tests := []struct {
		name     string
		intent   *ParsedIntent
		expected RiskLevel
	}{
		{
			name: "CREATE defaults to LOW",
			intent: &ParsedIntent{
				Action: ActionCreate,
				Target: ResourceTarget{Kind: "Deployment"},
			},
			expected: RiskLow,
		},
		{
			name: "UPDATE defaults to MEDIUM",
			intent: &ParsedIntent{
				Action: ActionUpdate,
				Target: ResourceTarget{Kind: "Deployment", Name: "test"},
			},
			expected: RiskMedium,
		},
		{
			name: "DELETE defaults to HIGH",
			intent: &ParsedIntent{
				Action: ActionDelete,
				Target: ResourceTarget{Kind: "Deployment", Name: "test"},
			},
			expected: RiskHigh,
		},
		{
			name: "INSPECT defaults to LOW",
			intent: &ParsedIntent{
				Action: ActionInspect,
				Target: ResourceTarget{Kind: "Pod", Name: "test"},
			},
			expected: RiskLow,
		},
		{
			name: "Unknown action defaults to LOW",
			intent: &ParsedIntent{
				Action: Action("UNKNOWN"),
				Target: ResourceTarget{Kind: "Deployment"},
			},
			expected: RiskLow,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			result := DefaultRiskLevel(tt.intent)
			if result != tt.expected {
				t.Errorf("expected %s, got %s", tt.expected, result)
			}
		})
	}
}

func TestIsNamespacedKind(t *testing.T) {
	tests := []struct {
		kind  string
		isNS  bool
	}{
		{"Deployment", true},
		{"Service", true},
		{"Pod", true},
		{"Node", false},
		{"Namespace", false},
		{"PersistentVolume", false},
		{"Unknown", false},
	}

	for _, tt := range tests {
		t.Run(tt.kind, func(t *testing.T) {
			result := IsNamespacedKind(tt.kind)
			if result != tt.isNS {
				t.Errorf("IsNamespacedKind(%s) = %v, want %v", tt.kind, result, tt.isNS)
			}
		})
	}
}